function parseCatalog(raw) {
  const parsed = JSON.parse(raw);
  if (!parsed || !Array.isArray(parsed.names)) {
    throw new Error("workspace name catalog must contain a names array");
  }
  return parsed.names.map((name, index) => {
    if (typeof name !== "string" || !name.trim()) {
      throw new Error(`workspace name at index ${index} must be a non-empty string`);
    }
    return name.trim();
  });
}

function tsString(value) {
  return JSON.stringify(value);
}

function pyString(value) {
  return JSON.stringify(value);
}

function renderTypeScript(names, hash) {
  const lines = names.map((name) => `  ${tsString(name)},`).join("\n");
  return `// Generated by scripts/generate-workspace-name-catalog.mjs.
// Source: catalogs/workspace-names/v1/animals.json.
// Catalog hash: ${hash}

export const WORKSPACE_ANIMAL_NAMES = [
${lines}
] as const;
`;
}

function renderServer(names, hash) {
  const lines = names.map((name) => `    ${pyString(name)},`).join("\n");
  return `# Generated by scripts/generate-workspace-name-catalog.mjs.
# Source: catalogs/workspace-names/v1/animals.json.
# Catalog hash: ${hash}

WORKSPACE_ANIMAL_NAMES = (
${lines}
)
`;
}

const raw = await readFile(catalogPath, "utf8");
const names = parseCatalog(raw).filter((name) => !deniedNames.has(name));
const hash = createHash("sha256").update(JSON.stringify(names)).digest("hex");

await writeFile(typeScriptOutputPath, renderTypeScript(names, hash));
await writeFile(serverOutputPath, renderServer(names, hash));
