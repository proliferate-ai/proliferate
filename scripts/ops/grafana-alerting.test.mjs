// Fake execFile: answers sts get-caller-identity with `account`, then
// delegates secretsmanager calls to `onSecret`.
function fakeAws(account, onSecret) {
  const calls = [];
  const impl = async (cmd, args) => {
    calls.push({ cmd, args });
    if (args[0] === "sts") {
      return { stdout: JSON.stringify({ Account: account }) };
    }
    return onSecret(cmd, args);
  };
  return { impl, calls };
}

