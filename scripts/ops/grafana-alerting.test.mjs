// Mimic the server's behavior on Alertmanager config POST: secureSettings
// values are encrypted away into secureFields markers, and Grafana REGENERATES
// the autogenerated routing subtree to hold exactly one child per receiver.
function encryptAmConfig(config) {
  const stored = JSON.parse(JSON.stringify(config));
  for (const receiver of stored.alertmanager_config.receivers || []) {
    for (const cfg of receiver.grafana_managed_receiver_configs || []) {
      if (cfg.secureSettings) {
        cfg.secureFields = { ...(cfg.secureFields || {}) };
        for (const key of Object.keys(cfg.secureSettings)) {
          cfg.secureFields[key] = true;
        }
        delete cfg.secureSettings;
      }
    }
  }
  const autogen = (stored.alertmanager_config.route?.routes || []).find((r) =>
    (r.object_matchers || []).some((m) => m[0] === "__grafana_autogenerated__"),
  );
  if (autogen) {
    autogen.routes = (stored.alertmanager_config.receivers || []).map((r) => ({
      object_matchers: [["__grafana_receiver__", "=", r.name]],
      receiver: r.name,
      group_by: ["grafana_folder", "alertname"],
    }));
  }
  return stored;
}

function jsonResponse(status, body) {
  return {
    ok: status >= 200 && status < 300,
    status,
    text: async () => (body === undefined ? "" : JSON.stringify(body)),
  };
}

const API = "/api/v1/provisioning";

// Fake fetch implementing the provisioning routes the client uses. Only the
// transport is faked; the client under test is the real one.
function fakeGrafanaFetch(state) {
  return async (url, init = {}) => {
    const method = init.method || "GET";
    const parsed = new URL(url);
    const p = parsed.pathname;
    state.requests.push({ method, url, path: p, headers: init.headers || {}, body: init.body });
    if (method !== "GET" && state.onWrite) state.onWrite(method, p);
    const body = init.body ? JSON.parse(init.body) : undefined;

    if (method === "GET" && p === `${API}/alert-rules`) return jsonResponse(200, state.rules);
    const ruleMatch = p.match(new RegExp(`^${API}/alert-rules/(.+)$`));
    if (ruleMatch) {
      const uid = decodeURIComponent(ruleMatch[1]);
      const idx = state.rules.findIndex((r) => r.uid === uid);
      if (method === "GET") return idx >= 0 ? jsonResponse(200, state.rules[idx]) : jsonResponse(404, {});
      if (method === "PUT") {
        if (idx < 0) return jsonResponse(404, {});
        state.rules[idx] = body;
        return jsonResponse(200, body);
      }
    }
    if (p === `${API}/contact-points`) {
      if (method === "GET") return jsonResponse(200, state.contactPoints);
      if (method === "POST") {
        const created = { ...body, uid: `cp${state.nextUid++}` };
        state.contactPoints.push(created);
        return jsonResponse(202, created);
      }
    }
    const cpMatch = p.match(new RegExp(`^${API}/contact-points/(.+)$`));
    if (cpMatch) {
      const uid = decodeURIComponent(cpMatch[1]);
      const idx = state.contactPoints.findIndex((cp) => cp.uid === uid);
      if (idx < 0) return jsonResponse(404, {});
      if (method === "PUT") {
        state.contactPoints[idx] = { ...body, uid };
        return jsonResponse(202, state.contactPoints[idx]);
      }
      if (method === "DELETE") {
        state.contactPoints.splice(idx, 1);
        return jsonResponse(202, {});
      }
    }
    if (method === "GET" && p === `${API}/policies`) return jsonResponse(200, state.notificationPolicy);
    if (p === ALERTMANAGER_CONFIG) {
      if (method === "GET") return jsonResponse(200, state.amConfig);
      if (method === "POST") {
        state.amConfig = encryptAmConfig(body);
        if (state.onAmPost) state.onAmPost(state);
        return jsonResponse(202, { message: "configuration created" });
      }
    }
    return jsonResponse(404, {});
  };
}

function realClient(state, token = "test-token-value") {
  return createGrafanaClient({ fetchImpl: fakeGrafanaFetch(state), tokenProvider: () => token });
}

